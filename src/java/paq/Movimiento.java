package paq;

/**
 *
 * @author Gerardo
 */
public class Movimiento {
    private int id_m, cantidad, precio;
    private String nom_c, nom_p;

    public int getId_m() {
        return id_m;
    }

    public void setId_m(int id_m) {
        this.id_m = id_m;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    public int getPrecio() {
        return precio;
    }

    public void setPrecio(int precio) {
        this.precio = precio;
    }

    public String getNom_c() {
        return nom_c;
    }

    public void setNom_c(String nom_c) {
        this.nom_c = nom_c;
    }

    public String getNom_p() {
        return nom_p;
    }

    public void setNom_p(String nom_p) {
        this.nom_p = nom_p;
    }     
    
    @Override
    public String toString(){
        String res = "ID_M: "+id_m+" Nom_c: "+nom_c+" Nom_P "+nom_p+" Cantidad: "+cantidad+" Precio: "+precio;
        return res;
    }
       
}
